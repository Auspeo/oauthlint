#!/usr/bin/env node
import { existsSync, readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { basename, dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
/**
 * AI-codegen auth benchmark runner.
 *
 * Scans every `gen/<tool>/` directory with the local OAuthLint CLI and writes a
 * comparison report (RESULTS.md + results.json). Each `gen/<tool>/` holds the
 * code an AI tool produced for the prompts in `prompts/` (matched by filename).
 *
 * Usage:
 *   pnpm build                # build the CLI first
 *   tsx benchmark/run.ts      # or: pnpm bench
 *
 * Add a tool: drop its generated files in `gen/<tool>/<prompt-id>.<ext>` and
 * re-run. Add a prompt: add `prompts/<id>.md` and regenerate code for it.
 */
import { execa } from 'execa';
import { parse as parseYaml } from 'yaml';

const HERE = dirname(fileURLToPath(import.meta.url));
const CLI = join(HERE, '..', 'cli', 'bin', 'oauthlint.js');
const PROMPTS_DIR = join(HERE, 'prompts');
const GEN_DIR = join(HERE, 'gen');

interface Prompt {
  id: string;
  language: string;
  domains: string[];
}
interface Finding {
  ruleId: string;
  severity: string;
  promptId: string;
}

function loadPrompts(): Prompt[] {
  return readdirSync(PROMPTS_DIR)
    .filter((f) => f.endsWith('.md'))
    .map((f) => {
      const raw = readFileSync(join(PROMPTS_DIR, f), 'utf8');
      const m = raw.match(/^---\n([\s\S]*?)\n---/);
      const fm = (m ? parseYaml(m[1]) : {}) as Record<string, unknown>;
      return {
        id: String(fm.id ?? basename(f, '.md')),
        language: String(fm.language ?? 'unknown'),
        domains: (fm.domains as string[]) ?? [],
      };
    })
    .sort((a, b) => a.id.localeCompare(b.id));
}

async function scanTool(toolDir: string): Promise<Finding[]> {
  const res = await execa('node', [CLI, 'scan', toolDir, '--json', '--fail-on', 'off'], {
    reject: false,
  });
  let report: { findings?: { ruleId: string; severity: string; filePath: string }[] };
  try {
    report = JSON.parse(res.stdout);
  } catch {
    return [];
  }
  return (report.findings ?? []).map((f) => ({
    ruleId: f.ruleId,
    severity: f.severity,
    // map the generated file back to its prompt id (filename without extension)
    promptId: basename(f.filePath).replace(/\.[^.]+$/, ''),
  }));
}

const HIGH = new Set(['HIGH', 'CRITICAL']);

async function main(): Promise<void> {
  const prompts = loadPrompts();
  const tools = readdirSync(GEN_DIR, { withFileTypes: true })
    .filter((e) => e.isDirectory())
    .map((e) => e.name)
    .sort();

  const byTool: Record<string, Finding[]> = {};
  for (const tool of tools) byTool[tool] = await scanTool(join(GEN_DIR, tool));

  // ---- aggregate ----
  const summary = tools.map((tool) => {
    const fs = byTool[tool];
    const flagged = new Set(fs.map((f) => f.promptId)).size;
    const high = fs.filter((f) => HIGH.has(f.severity)).length;
    return { tool, total: fs.length, flagged, high };
  });

  const lines: string[] = [];
  lines.push('# AI-codegen auth benchmark — results');
  lines.push('');
  lines.push(
    `Generated by \`benchmark/run.ts\` over **${prompts.length} prompts** and **${tools.length} tool(s)**. Each tool was asked to implement the prompts in \`prompts/\` under a neutral "ship this feature" framing (no security hints); the output was then scanned with the OAuthLint rule pack.`,
  );
  lines.push('');
  lines.push('## Scoreboard');
  lines.push('');
  lines.push('| Tool | Prompts with findings | Total findings | HIGH-severity |');
  lines.push('|------|:--:|:--:|:--:|');
  for (const s of summary) {
    lines.push(`| \`${s.tool}\` | ${s.flagged}/${prompts.length} | ${s.total} | ${s.high} |`);
  }
  lines.push('');

  for (const tool of tools) {
    lines.push(`## \`${tool}\` — per prompt`);
    lines.push('');
    for (const p of prompts) {
      const fs = byTool[tool].filter((f) => f.promptId === p.id);
      const tag = fs.length ? `⚠ ${fs.length}` : '✓ clean';
      lines.push(`- **${p.id}** (${p.language}) — ${tag}`);
      for (const f of fs) lines.push(`  - \`${f.ruleId}\` [${f.severity}]`);
    }
    lines.push('');
  }

  lines.push(
    '<!-- Regenerate: pnpm build && tsx benchmark/run.ts. Methodology: benchmark/README.md. -->',
  );
  lines.push('');

  writeFileSync(join(HERE, 'RESULTS.md'), lines.join('\n'), 'utf8');
  writeFileSync(
    join(HERE, 'results.json'),
    `${JSON.stringify({ prompts: prompts.length, tools: summary, findings: byTool }, null, 2)}\n`,
  );
  console.log(`✓ Scanned ${tools.length} tool(s) over ${prompts.length} prompts.`);
  for (const s of summary)
    console.log(
      `  ${s.tool}: ${s.flagged}/${prompts.length} flagged, ${s.total} findings, ${s.high} HIGH`,
    );
  if (!existsSync(CLI)) console.warn('! CLI not found — run `pnpm build` first.');
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
